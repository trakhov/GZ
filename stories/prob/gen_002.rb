def choose_names(names)
	domain = names.shuffle.pop.shuffle
	[domain.pop, domain.pop]
end

def choose_bounds
	a = rand(1..20)
	b = a + rand(1..4)
	strt = "%d:00" % a
	eend = "%d:00" % b
	[strt, eend, b-a]
end

def choose_times(bounds)
	m = bounds[2]
	t1 = 10 * rand(m+1..m+4)
	t2 = t1
	while t2 == t1
		t2 = 10 * rand(m+1..m+4)
	end
	[t1, t2]
end

def options
	names = choose_names $names
	bounds = choose_bounds 
